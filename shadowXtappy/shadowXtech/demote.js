import config from '../../config.cjs';

const demote = async (m, gss) => {
  try {
    const prefix = config.PREFIX;
const cmd = m.body.startsWith(prefix) ? m.body.slice(prefix.length).split(' ')[0].toLowerCase() : '';
const text = m.body.slice(prefix.length + cmd.length).trim();

    const validCommands = ['demote', 'unadmin'];

    if (!validCommands.includes(cmd)) return;


    if (!m.isGroup) return m.reply("*s’∞Œ±’™÷Ö’° x‘µ“Ωc’∞ —ïŒ±—É—ï —Ç–ΩŒπ—ï ŒπŒ± g—èœÉœÖœÅ ¬¢œÉ–º–ºŒ±Œ∑‚àÇ*");
    const groupMetadata = await gss.groupMetadata(m.from);
    const participants = groupMetadata.participants;
    const botNumber = await gss.decodeJid(gss.user.id);
    const botAdmin = participants.find(p => p.id === botNumber)?.admin;
    const senderAdmin = participants.find(p => p.id === m.sender)?.admin;

    if (!botAdmin) return m.reply("*Íú± ú·¥Ä·¥Ö·¥è·¥°-x·¥õ·¥á·¥Ñ ú –ºœÖ—ï—Ç –≤—î Œ±‚àÇ–ºŒπŒ∑*");
    if (!senderAdmin) return m.reply("*—ÉœÉœÖ Œ±—è—î Œ∑œÉ—Ç Œ±‚àÇ–ºŒπŒ∑ –ºœÉ—Ç–Ω—î—è∆íœÖ¬¢–∫—î—è*");

    if (!m.mentionedJid) m.mentionedJid = [];

    if (m.quoted?.participant) m.mentionedJid.push(m.quoted.participant);

    const users = m.mentionedJid.length > 0
      ? m.mentionedJid
      : text.replace(/[^0-9]/g, '').length > 0
      ? [text.replace(/[^0-9]/g, '') + '@s.whatsapp.net']
      : [];

    if (users.length === 0) {
      return m.reply("*—è—îœÉ‚Ñì—É Œ± ¬¢œÉ–º—èŒ±‚àÇ—î (œÖ—ï—î—è)–º—î—ï—ïŒ±g—î —ÇœÉ ‚àÇ—î–ºœÉ—Ç—î*");
    }

    const validUsers = users.filter(Boolean);

    await gss.groupParticipantsUpdate(m.from, validUsers, 'demote')
      .then(() => {
        const demotedNames = validUsers.map(user => `@${user.split("@")[0]}`);
        m.reply(`*œÖ—ï—î—è ${demotedNames} ‚àÇ—î–ºœÉ—Ç—î‚àÇ ŒπŒ∑ ${groupMetadata.subject}*`);
      })
      .catch(() => m.reply('‚ùå Failed to demote user(s) in the group.'));
  } catch (error) {
    console.error('Error:', error);
    m.reply('‚ùå An error occurred while processing the command.');
  }
};

